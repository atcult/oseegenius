package com.atc.osee.web.model;

/**
 * Model for Sebina sol.
 * 
 * @author Natascia Bianchini
 * @since 1.2
 *
 */
public class SubscriptionSebina {
	
	private Integer yearId;
	private Integer yearDesc;
	private String collocation;
	private String inventory;
	private String possessed;
	private String subcriptionCode;
	private String label;
	private String date;
	private String status;
	
	
	/**
	 * @return the yearId
	 */
	public Integer getYearId() {
		return yearId;
	}
	/**
	 * @param yearId the yearId to set
	 */
	public void setYearId(Integer yearId) {
		this.yearId = yearId;
	}
	/**
	 * @return the yearDesc
	 */
	public Integer getYearDesc() {
		return yearDesc;
	}
	/**
	 * @param yearDesc the yearDesc to set
	 */
	public void setYearDesc(Integer yearDesc) {
		this.yearDesc = yearDesc;
	}
	/**
	 * @return the collocation
	 */
	public String getCollocation() {
		return collocation;
	}
	/**
	 * @param collocation the collocation to set
	 */
	public void setCollocation(String collocation) {
		this.collocation = collocation;
	}
	/**
	 * @return the inventory
	 */
	public String getInventory() {
		return inventory;
	}
	/**
	 * @param inventory the inventory to set
	 */
	public void setInventory(String inventory) {
		this.inventory = inventory;
	}
	/**
	 * @return the possessed
	 */
	public String getPossessed() {
		return possessed;
	}
	/**
	 * @param possessed the possessed to set
	 */
	public void setPossessed(String possessed) {
		this.possessed = possessed;
	}
	/**
	 * @return the subcriptionCode
	 */
	public String getSubcriptionCode() {
		return subcriptionCode;
	}
	/**
	 * @param subcriptionCode the subcriptionCode to set
	 */
	public void setSubcriptionCode(String subcriptionCode) {
		this.subcriptionCode = subcriptionCode;
	}
	/**
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}
	/**
	 * @param label the label to set
	 */
	public void setLabel(String label) {
		this.label = label;
	}
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	

}
